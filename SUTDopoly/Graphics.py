# Play during TurnFunctions.move_board()
def Board_Animation(board, initial_pos, final_pos, move_str):
	length = len(board)
	steps_index = [n % length for n in range(initial_pos, final_pos)]


  
  # This portion was modified from
  # the code found at the following link:
  # https://stackoverflow.com/questions/2084508/clear-terminal-in-python
	if os.name == 'nt':
		clear = 'cls'
	else:
		clear = 'clear'



	def getProp(n):
		prop_name = board[n].titleName
		return prop_name

	def ClearPrint():
		os.system(clear)
		print('\n' + move_str)


	ClearPrint()
	time.sleep(1)

	for n in steps_index:

		# Short for next: finds next index
		nxt = (n+1) % length
		nxtnxt = (n+2) % length # For the fifth frame (first frame repetition)

		ClearPrint()
		print('\n'+first_frame.format(getProp(nxt), getProp(n), getProp(n-1), nxt, n, n-1) + '\n ')
		time.sleep(0.1)

		ClearPrint() 
		print('\n'+second_frame.format(getProp(nxt), getProp(n), nxt, n) + '\n ')
		time.sleep(0.02)

		ClearPrint() 
		print('\n'+third_frame.format(getProp(nxt), getProp(n), nxt, n) + '\n ')
		time.sleep(0.02)

		ClearPrint()
		print('\n'+fourth_frame.format(getProp(nxt), getProp(n), nxt, n) + '\n ')
		time.sleep(0.02)

		ClearPrint()
		print('\n'+first_frame.format(getProp(nxtnxt), getProp(nxt), getProp(n), nxtnxt, nxt, n) + '\n ')
		time.sleep(0.2)



